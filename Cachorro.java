public class Cachorro extends Animal {
  String pelagem;
  int porte;
  double peso;

  public Cachorro(String nome, int idade, String cor, String pelagem, int porte, double peso, String raca) {
    this.nome = nome;
    this.idade = idade;
    this.cor = cor;
    this.pelagem = pelagem;
    this.porte = porte;
    this.peso = peso;
    this.raca = raca;
  } 
}
